const firstNumber = Number.parseFloat(prompt('Введите первое число'));
const twoNumber = Number.parseFloat(prompt('Введите второе число'));

alert((firstNumber + twoNumber).toFixed(1));
alert((firstNumber - twoNumber).toFixed(1));
alert((firstNumber / twoNumber).toFixed(1));
alert((firstNumber * twoNumber).toFixed(1));
alert((firstNumber % twoNumber).toFixed(1));